public class Superman extends HomoSapien implements Flyer{
   public Superman(){
      System.out.println("He is Superman");
   }
   public void takeOff(){
      System.out.println("Superman can takeoff anywhere");
   }
   public void land(){
      System.out.println("Superman can land on a building");
   }
   public void fly(){
      System.out.println("Superman can fly");
   }
   public void leapBuilding(){
      System.out.println("Superman can leap buildings");
   }
   public void stopBullet(){
      System.out.println("Superman can stop a bullet");
   }
}