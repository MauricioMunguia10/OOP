public class Bird implements Flyer{
   public Bird(){
      System.out.println("It is a bird");
   }
   public void takeOff(){
      System.out.println("A bird can takeoff anywhere");
   }
   public void land(){
      System.out.println("A bird can land on a building");
   }
   public void fly(){
      System.out.println("A bird can fly");
   }
   public void buildNest(){
      System.out.println("A bird can build a nest");
   }
   public void layEggs(){
      System.out.println("A bird can lay eggs");
   }
   public void eat(){
      System.out.println("A bird can eat");
   }
   
}